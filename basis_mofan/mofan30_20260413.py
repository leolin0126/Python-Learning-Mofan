# copy功能 和 deepcopy功能
import copy
a = [1,2,3]
b = a
print(id(a))
print(id(b))
b[0] = 11
print(a)
a[1] = 22
print(b)
# copy功能（shallow copy，浅复制功能）
c = copy.copy(a)
print(id(a)==id(c))  # 此时变量a与变量c不在同一个位置
c[1] = 222222
print(a)
print(c)  # 从输出结果看，此时变量a的值不会因变量c中重新赋值而改变

a = [1,2,[3,4]]
d = copy.copy(a)
print(id(a)==id(d))  # 变量a和变量d不在同一个位置
print(id(a[2])==id(d[2]))  # 变量a的某个值和变量d的某个值在同一个位置
a[0] = 11
print(a)
print(d)
a[2][0] = 333
print(a)
print(d)

# 解析：

# 不可变对象（整数、字符串、元组等）：如果对原列表中的不可变元素重新赋值（即让列表的某个槽位指向一个新对象），
# 另一个列表不受影响，因为两个列表的槽位本来就是独立的引用。

# 可变对象（列表、字典、集合等）：如果只是修改这个可变对象的内部内容（比如改子列表的元素），
# 那么所有引用该可变对象的地方都会看到变化。

# 不可变对象：整数、字符串、元组等。你无法修改它们内部的状态，只能让变量指向新的对象。
# 可变对象：列表、字典、集合等。你可以修改它们的内容（添加、删除、改变元素），而不改变对象本身的身份。

# 因此，不能简单说“a[0] 是不可变对象，a[2][0] 是可变对象”，因为两者都是不可变对象。
# 正确的说法是：修改外层列表的顶层元素是替换引用，不影响副本；修改嵌套的可变对象内部元素，则会影响所有引用该对象的副本。

e = copy.deepcopy(a)
print(id(a[2])==id(e[2])) 







